import Foundation

/// The chat message object used for each chat conversation message.
public struct ChatMessage: Codable, Identifiable {
    private enum CodingKeys: String, CodingKey {
        case role, content
        case functionCall = "function_call"
        case toolCalls = "tool_calls"
        case toolCallId = "tool_call_id"
    }

    /// ID used for iterating through list of chat messages
    public var id = UUID().uuidString

    /// The person sending the message
    public let role: ChatRole

    /// The message itself
    public let content: String?

    /// The name and arguments of a function that should be called, as generated by the model.
    public let functionCall: FunctionCall?
    
    public let toolCalls: [ToolCall]?
    
    public let toolCallId: String?

    public init(role: ChatRole, content: String? = nil, functionCall: FunctionCall? = nil, toolCalls: [ToolCall]? = [], toolCallId: String? = nil) {
        self.role = role
        self.content = content
        self.functionCall = functionCall
        self.toolCalls = toolCalls
        self.toolCallId = toolCallId
    }

    public var body: [String: Any] {
        if let content {
            return [
                "role": self.role.rawValue,
                "content": content,
                "tool_call_id": self.toolCallId ?? ""
            ]
        }
        if let toolCalls {
            return [
                "role": self.role.rawValue,
                "content": content,
                "tool_calls": toolCalls.map { $0.body },
                "tool_call_id": self.toolCallId ?? ""
            ]
        }
        return  [
            "role": self.role.rawValue,
            "tool_call_id": self.toolCallId ?? ""
        ]
    }
}
